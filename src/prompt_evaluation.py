from src.utils import add_user_message, add_assistant_message, chat
from src.utils import get_client, get_model
from statistics import mean
import json
import ast
import re


client = get_client()
model = get_model()


def generate_dataset():
    prompt = """
Generate a evaluation dataset for a prompt evaluation. The dataset will be
used to evaluate prompts that generate Python, JSON, or Regex specifically
for AWS-related tasks. Generate an array of JSON objects,
each representing task that requires Python, JSON, or a Regex to complete.

Example output:
```json
[
    {
        "task": "Description of task",
        "format": "json" or "python" or "regex"
        "solution_criteria": "Key criteria for evaluating the solution"
    },
    ...additional
]
```

* Focus on tasks that can be solved by writing a single Python function,
a single JSON object, or a regular expression.
* Focus on tasks that do not require writing much code

Please generate 3 objects.
"""

    messages = []
    add_user_message(messages, prompt)
    add_assistant_message(messages, "```json")
    text = chat(
        messages=messages,
        client=client,
        model=model,
        stop_sequences=["```"]
    )
    return json.loads(text)


def run_prompt(test_case: dict[str, str]) -> str:
    """
    Merges the prompt and test case input, then returns the result

    Args:
        test_case (dict[str, str]): A dictionary containing the task and any
            additional information needed to solve the task.

    Returns:
        str: The output generated by the model after running the prompt.
    """

    prompt = f"""
Please solve the following task:

{test_case["task"]}

* Respond only with Python, JSON, or a plain Regex
* Do not add any comments or commentary or explanation
"""

    messages = []
    add_user_message(messages=messages, content=prompt)
    add_assistant_message(messages=messages, content="```code")
    output = chat(
        messages=messages,
        client=client,
        model=model,
        temperature=0.0,
        stop_sequences=["```"]
    )
    return output


def run_test_case(test_case: dict[str, str]) -> dict[str, str | int | dict]:
    """
    Calls run_prompt, then grades the result

    Args:
        test_case (dict[str, str]): A dictionary containing the task and any
            additional information needed to solve the task.

    Returns:
        dict[str, str | int | dict]: A dictionary containing the output,
            test case, and score.
    """
    output = run_prompt(test_case)

    # Grade the output using the model
    model_grade = grade_by_model(test_case, output)
    model_score = model_grade["score"]
    reasoning = model_grade["reasoning"]

    syntax_score = grade_syntax(output, test_case)

    score = (model_score + syntax_score) / 2

    return {
        "output": output,
        "test_case": test_case,
        "score": score,
        "reasoning": reasoning,
    }


def run_eval(
    dataset: list[dict[str, str]]
) -> list[dict[str, str | int | dict]]:
    """
    Loads the dataset and calls run_test_case with each case

    Args:
        dataset (list[dict[str, str]]): A list of dictionaries, each
            containing a task and any additional information needed to solve
            the task.

    Returns:
        list[dict[str, str | int | dict]]: A list of dictionaries, each
            containing the output, test case, and score.
    """
    results = []

    for test_case in dataset:
        result = run_test_case(test_case)
        results.append(result)

    average_score = mean(result["score"] for result in results)
    print(f"Average Score: {average_score}")

    return results


def grade_by_model(test_case: dict, output: str) -> dict:
    """
    Uses the model to grade the output of a test case.

    Args:
        test_case (dict): The test case to grade.
        output (str): The output generated by the model for the test case.

    Returns:
            dict: The evaluation results.
    """
    eval_prompt = f"""
You are an expert AWS code reviewer. Your task is to evaluate the following
AI-generated solution.

Original Task:
<task>
{test_case["task"]}
</task>

Solution to Evaluate:
<solution>
{output}
</solution>

Consider the solution criteria provided below when evaluating the solution:
<criteria>
{test_case["solution_criteria"]}
</criteria>

Output Format
Provide your evaluation as a structured JSON object with the following fields,
in this specific order:
- "strengths": An array of 1-3 key strengths
- "weaknesses": An array of 1-3 key areas for improvement
- "reasoning": A concise explanation of your overall assessment
- "score": A number between 1-10

Respond with JSON. Keep your response concise and direct.
Example response shape:
{{
    "strengths": string[],
    "weaknesses": string[],
    "reasoning": string,
    "score": number
}}
    """

    messages = []
    add_user_message(messages, eval_prompt)
    add_assistant_message(messages, "```json")
    eval_text = chat(
        messages=messages,
        client=client,
        model=model,
        stop_sequences=["```"]
    )
    return json.loads(eval_text)


def validate_json(text: str) -> int:
    """
    Validates if the given text is a valid JSON.

    Args:
        text (str): The text to validate.

    Returns:
        int: 10 if the text is valid JSON, otherwise 0.
    """

    try:
        json.loads(text.strip())
        return 10
    except json.JSONDecodeError:
        return 0


def validate_python(text: str) -> int:
    """
    Validates if the given text is valid Python code.

    Args:
        text (str): The text to validate.

    Returns:
        int: 10 if the text is valid Python code, otherwise 0.
    """

    try:
        ast.parse(text.strip())
        return 10
    except SyntaxError:
        return 0


def validate_regex(text: str) -> int:
    """
    Validates if the given text is a valid regex pattern.

    Args:
        text (str): The text to validate.

    Returns:
        int: 10 if the text is a valid regex pattern, otherwise 0.
    """

    try:
        re.compile(text.strip())
        return 10
    except re.error:
        return 0


def grade_syntax(response: str, test_case: dict) -> int:
    """
    Grades the syntax of the response based on the expected format in the test
    case.

    Args:
        response (str): The output generated by the model for the test case.
        test_case (dict): The test case containing the expected format.

    Returns:
        int: A score between 0 and 10 based on the validity of the syntax.
    """

    format = test_case["format"]
    if format == "json":
        return validate_json(response)
    elif format == "python":
        return validate_python(response)
    else:
        return validate_regex(response)
